def make_mop(base, offset=0):
    mop_anim = []
    # 右側のモップ部分
    # SUPERの時は 6, 7 が右側にあるが、それを消してモップにする
    mop_head_pattern = [
        ".......",
        "...6...",
        "..686..",
        ".68886.",
        "..686..",
        "...6...",
        ".......",
        "......."
    ]
    for y, line in enumerate(base):
        chars = list(line)
        # 帽子(3) -> (8)
        if y < 4:
            for i in range(len(chars)):
                if chars[i] == '3':
                    chars[i] = '8'
        # ネクタイ(5) -> (3)
        if y == 5:
            for i in range(len(chars)):
                if chars[i] == '5':
                    chars[i] = '3'
        # 服(4) -> (8)
        for i in range(len(chars)):
            if chars[i] == '4':
                chars[i] = '8'
        
        # 掃除機のパイプ(6, 7)を透明(.)にする
        for i in range(len(chars)):
            if chars[i] in ('6', '7'):
                chars[i] = '.'
                
        # 右側にモップを描画 (y=9から16あたりにモップのヘッド)
        # 棒は手から下へ
        if 8 <= y <= 16:
            mop_y = y - 9
            if mop_y >= 0 and mop_y < len(mop_head_pattern):
                pat = mop_head_pattern[mop_y]
                # 右端13〜19にマージ
                for i in range(7):
                    if pat[i] != '.':
                        chars[13+i] = pat[i]
        
        mop_anim.append("".join(chars))
    return mop_anim

