import os
from PIL import Image, ImageDraw

# パレット設定
palette = {
    '.': (0, 0, 0, 0),       # 透明
    '1': (255, 204, 153, 255), # 肌色
    '2': (255, 255, 255, 255), # 白
    '3': (51, 51, 51, 255),    # 黒
    '4': (51, 102, 204, 255),  # 青
    '5': (204, 51, 51, 255),   # 赤
    '6': (139, 69, 19, 255),   # 茶
    '7': (153, 153, 153, 255)  # グレー
}

def render_sprite(data, filename, pixel_size=4):
    height = len(data)
    width = len(data[0]) if height > 0 else 0
    img = Image.new('RGBA', (width * pixel_size, height * pixel_size), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    
    for y, row in enumerate(data):
        for x, char in enumerate(row):
            color = palette.get(char, (0, 0, 0, 0))
            if color[3] > 0:
                draw.rectangle([
                    x * pixel_size, y * pixel_size, 
                    (x + 1) * pixel_size - 1, (y + 1) * pixel_size - 1
                ], fill=color)
    
    # 拡大して見やすくする (さらに4倍)
    img = img.resize((width * pixel_size * 4, height * pixel_size * 4), Image.NEAREST)
    img.save(filename)

